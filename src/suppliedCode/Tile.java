package suppliedCode;

public class Tile {
	private String player;
	private String direction;
	private int x;
	private int y;
	
	public String getPlayer() {
		return player;
	}
	
	public String getDirection() {
		return direction;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
}
